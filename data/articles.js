export const getArticleById = (id) => {
  return articles.find(article => article.id === parseInt(id));
};

export const getHeadlineArticle = () => {
  return articles.find(article => article.isHeadline);
};

export const getRegularArticles = () => {
  return articles.filter(article => !article.isHeadline);
};

export const getRelatedArticles = (currentId, category, limit = 3) => {
  return articles
    .filter(article => article.id !== parseInt(currentId) && article.category === category)
    .slice(0, limit);
};

export const getPopularArticles = (limit = 5) => {
  return [...articles]
    .sort((a, b) => b.views - a.views)
    .slice(0, limit);
};

export const getSubHeadlineArticles = (limit = 2) => {
  return articles
    .filter(article => !article.isHeadline)
    .sort((a, b) => new Date(b.date) - new Date(a.date))
    .slice(0, limit);
};
